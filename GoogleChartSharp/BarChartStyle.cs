namespace GoogleChartSharp
{
    /// <summary>
    /// Bar chart style when using multiple data sets
    /// </summary>
    public enum BarChartStyle
    {
        /// <summary>
        /// Multiple data sets will be stacked.
        /// </summary>
        Stacked,

        /// <summary>
        /// Multiple data sets will be grouped.
        /// </summary>
        Grouped
    }
}