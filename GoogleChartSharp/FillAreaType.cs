namespace GoogleChartSharp
{
    /// <summary>
    /// Specify area fill behavior
    /// </summary>
    public enum FillAreaType
    {
        /// <summary>
        /// All area under the line will be filled
        /// </summary>
        SingleLine,

        /// <summary>
        /// The area between this line and the next will be filled
        /// </summary>
        MultiLine
    }
}